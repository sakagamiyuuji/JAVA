public class penugasan{

  public static void main(String[] args) {
    int a = 10;
    int b = 5;

    System.out.println(a);
    a = b;
    System.out.println(a);
    a += b;
    System.out.println(a);
    b -= a;
    System.out.println(b);
    b *= b;
    System.out.println(b);
    b /= a;
    System.out.println(b);
    a %= b;
    System.out.println(a);
  }
}
