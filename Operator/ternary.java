public class ternary{

  public static void main(String[] args) {
    boolean hujan = false;

    System.out.println(hujan? "bawa payung" : "tidak usah bawa payung");

    String nama = "udin";

    String acces = nama == "roby" ? "selamat datang roby" : "siapa lu?";

    System.out.println(acces);
  }
}
